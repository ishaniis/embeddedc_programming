/*
Concept of Typedef()
*/

/*
Typedef -> alternative names to exisiting data types
*/

/*
Syntax of Typedef:

typedef <previous_name> <new_name_or_alias_name>
*/

/*
Example: 
typedef unsigned Long ul;
typedef float fl;

ul l1, l2, l3; // Alias name of unsigned long ul
fl v1,v2,v3; //Alias name of float as fl
*/